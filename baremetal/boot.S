.section .text.boot
.global _start_boot
_start_boot:
    /* Setup stack to point to end of 128MB RAM block */
    li sp, 0x88000000

    /* Jump to C code */
    call _start

    /* Halt on return */
1:
    wfi
    j 1b

/* Guest payload embedded as .data blob */
.section .data
.global guest_payload_start
.global guest_payload_end
.align 8
guest_payload_start:
    .incbin "guest_payload.elf"
guest_payload_end:

/* Root of Trust Ed25519 public key (32 bytes, burned into hypervisor) */
.global rot_public_key
.align 8
rot_public_key:
    .incbin "rot_pub.key"

/* Guest Ed25519 signature (64 bytes, generated by sign_guest.py) */
.global guest_signature
.align 8
guest_signature:
    .incbin "guest_sig.bin"
